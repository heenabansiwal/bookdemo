package bookpojo;

public class Admintablepojo {
	private int adminid;
	private String adminname;
	private String adminaddress;
	private String adminpassword;
	private long adminphhono;
	private String admineamil;
	private String adminpost;
	public int getAdminid() {
		return adminid;
	}
	public void setAdminid(int adminid) {
		this.adminid = adminid;
	}
	public String getAdminname() {
		return adminname;
	}
	public void setAdminname(String adminname) {
		this.adminname = adminname;
	}
	public String getAdminaddress() {
		return adminaddress;
	}
	public void setAdminaddress(String adminaddress) {
		this.adminaddress = adminaddress;
	}
	public String getAdminpassword() {
		return adminpassword;
	}
	public void setAdminpassword(String adminpassword) {
		this.adminpassword = adminpassword;
	}
	public long getAdminphhono() {
		return adminphhono;
	}
	public void setAdminphhono(long adminphhono) {
		this.adminphhono = adminphhono;
	}
	public String getAdmineamil() {
		return admineamil;
	}
	public void setAdmineamil(String admineamil) {
		this.admineamil = admineamil;
	}
	public String getAdminpost() {
		return adminpost;
	}
	public void setAdminpost(String adminpost) {
		this.adminpost = adminpost;
	}
	

}
